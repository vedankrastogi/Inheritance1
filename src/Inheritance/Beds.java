package Inheritance;

public class Beds {
    private int width;
    private int height;
    private int size;

    public Beds(int width, int height, int size) {
        this.width = width;
        this.height = height;
        this.size = size;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getSize() {
        return size;
    }
}
